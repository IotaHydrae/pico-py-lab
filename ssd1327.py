class ssd1327(object):
    def __init__(self):
        self.spi = SPI(0, baudrate=8000000, sck=Pin(18), mosi=Pin(19))

        # self.busy = Pin(12, mode=Pin.IN)
        self.rst = Pin(15, mode=Pin.OUT, value=1)
        self.dc = Pin(14, mode=Pin.OUT, value=1)
        self.cs = Pin(13, mode=Pin.OUT, value=1)

    def spi_write8(self, b):
        self.spi.write(bytearray([b]))
        # print(b)
        pass

    def write_cmd(self, cmd):
        self.dc.low()
        self.cs.low()
        self.spi_write8(cmd)
        self.cs.high()
        pass

    def write_data(self, data):
        self.dc.high()
        self.cs.low()
        self.spi_write8(data)
        self.cs.high()
        pass

    def write_data_buffered(self, buf):
        self.dc.high()
        self.cs.low()
        for val in buf:
            self.spi_write8(val)
        self.cs.high()

    def write_reg(self, *opts):
        reg = opts[0]
        # print("reg: ", reg)
        self.write_cmd(reg)

        if len(opts) == 1:
            return

        opts = opts[1:]
        for val in opts:
            # print("val: ", val)
            self.write_data(val)

    def init_display(self):
        self.reset()

        self.write_reg(0xae)

        self.write_reg(0x15)
        self.write_reg(0x00)
        self.write_reg(0x7f)

        self.write_reg(0x75)
        self.write_reg(0x00)
        self.write_reg(0x7f)

        self.write_reg(0x81)
        self.write_reg(0x80)

        self.write_reg(0xa0)
        self.write_reg(0x51)

        self.write_reg(0xa1)
        self.write_reg(0x00)

        self.write_reg(0xa2)
        self.write_reg(0x00)

        self.write_reg(0xa4)
        self.write_reg(0xa8)
        self.write_reg(0x7f)

        self.write_reg(0xb1)
        self.write_reg(0xf1)

        self.write_reg(0xb3)
        self.write_reg(0x00)

        self.write_reg(0xab)
        self.write_reg(0x01)

        self.write_reg(0xb6)
        self.write_reg(0x0f)

        self.write_reg(0xbe)
        self.write_reg(0x0f)

        self.write_reg(0xbc)
        self.write_reg(0x08)

        self.write_reg(0xd5)
        self.write_reg(0x62)
        
        self.write_reg(0xfd)
        self.write_reg(0x12)

        time.sleep_ms(200)
        self.write_reg(0xaf)

